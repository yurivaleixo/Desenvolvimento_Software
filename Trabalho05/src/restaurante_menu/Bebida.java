package restaurante_menu;

public class Bebida {
	
	private String nome;
	private double preço;

	public void setNome(String nome) {
		this.nome = nome;
	}
	 	
	public String getNome() {
		return nome;
	}

	public void setPreço(double preço) {
		this.preço = preço;
	}

	public double getPreço() {
		return preço;
	}
 
	
}
